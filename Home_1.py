"""Задача 6: Вы пользуетесь общественным транспортом? Вероятно, вы расплачивались за проезд и получали билет с номером. 
Счастливым билетом называют такой билет с шестизначным номером, где сумма первых трех  цифр равна сумме последних трех. Т.е. билет
с номером 385916 – счастливый, т.к. 3+8+5=9+1+6. Вам требуется написать программу, которая проверяет счастливость билета.
*Пример:*
385916 -> yes
123456 -> no"""

num = int(input("Введите шестизначное число: "))
if 99999 < num < 999999:
    digit1 = num // 100000
    digit2 = num // 10000 % 10
    digit3 = num // 1000 % 100 % 10
    digit4 = num // 100 % 1000 % 100 % 10
    digit5 = num // 10 % 10000 % 1000 % 100 % 10
    digit6 = num % 10
    if digit1 + digit2 + digit3 == digit4 + digit5 + digit6:
        print("YES")
    else:
        print("NO")
else:
    print("Введенные данные не корректны")


