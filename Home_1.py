"""Задача 2: Найдите сумму цифр трехзначного  числа.
*Пример:*
123 -> 6 (1 + 2 + 3)
100 -> 1 (1 + 0 + 0) |"""

num = int(input("Введите трехзнач. число: "))
sum = 0

if 99<num<999:

    while num > 0:
        digit = num % 10
        sum = sum + digit
        num = num // 10

    print(f"Сумма цифр числа {num}  равна {sum}")
else:
    print("Введенные данные не корректны")


